def apply_fixes(text: str) -> str:
    # 1. Word-by-word replacements (sort by length desc to avoid partial matches)
    items = sorted(WORD_FIXES.items(), key=lambda kv: -len(kv[0]))
    for src, dst in items:
        if src == dst:
            continue
        pattern = r"\b" + re.escape(src) + r"\b"
        text = re.sub(pattern, dst, text)

    # 2. Preposition "a" -> "a"
    # Strategie : remplacer " a X" -> " a X" pour tous les contextes courants,
    # sauf quand "a" est l'auxiliaire (suivi d'un participe passe).
    #
    # Etape A : marque "Y a PARTICIPE" (auxiliaire) avec un sentinel APRES le 'a'
    # pour que les patterns suivants ne le matchent pas.
    # Sentinel : tilde + Z (improbable dans le texte).
    SENTINEL = "~Z~"
    followers_pattern = "(?:" + "|".join(
        re.escape(f) for f in AVOIR_FOLLOWERS
    ) + ")"
    text = re.sub(
        r"\b([a-zA-Z'\-]+)\s+a\s+(" + followers_pattern + r")\b",
        lambda m: f"{m.group(1)} a{SENTINEL} {m.group(2)}",
        text,
    )
    # Cas "n'a ", "qu'a " (apostrophe directement)
    text = re.sub(
        r"(n|qu|qui|c|s)'a\s+(" + followers_pattern + r")\b",
        lambda m: f"{m.group(1)}'a{SENTINEL} {m.group(2)}",
        text,
    )

    # Etape B : remplace " a " -> " a "  partout sauf protection sentinel

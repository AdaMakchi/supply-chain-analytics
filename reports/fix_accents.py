        (r"(\s)a million\b", r"\1à million"),
        # nombres
        (r"(\s)a (\d)", r"\1à \2"),
        # "compare a", "compares a", "remplacer a", etc.
        (r"(\s)a venir\b", r"\1à venir"),
        # "a l'horizon", "a l'echelle", etc deja capture par "a l'"
        # Mots usuels apres "a" : ETAT, LIEU, ECHELLE
        (r"(\s)a echelle\b", r"\1à échelle"),
        (r"(\s)a maille\b", r"\1à maille"),
        # Quelques tournures
        (r"(\s)a hauteur\b", r"\1à hauteur"),
        (r"(\s)a charge\b", r"\1à charge"),
